/* ── State ─────────────────────────────────────────────────── */
let ENTRIES     = [];
let currentSort = 'likes';
let searchQuery = '';
let lbEntry     = null;
let lbIndex     = 0;

/* ── Load data ──────────────────────────────────────────────── */
async function loadData() {
  try {
    const resp = await fetch('/cards/mascot/mascot-data.json');
    if (!resp.ok) throw new Error('not found');
    const json = await resp.json();
    ENTRIES = json.entries || [];

    // Update "generated at" footer
    if (json.generatedAt) {
      const d = new Date(json.generatedAt);
      document.getElementById('updatedAt').textContent =
        d.toLocaleDateString('en-GB', { day: 'numeric', month: 'short', year: 'numeric' }) +
        ' ' + d.toLocaleTimeString('en-GB', { hour: '2-digit', minute: '2-digit' }) + ' UTC';
    }
  } catch {
    // JSON not yet generated → use sample data so the page isn't empty
    console.info('mascot-data.json not found, using sample data.');
    ENTRIES = SAMPLE_ENTRIES;
  }
  render();
  showWinnerModal();
}

/* ── Winner modal ───────────────────────────────────────────── */
function showWinnerModal() {
  const winners = WINNER_TWEET_IDS.map(tid =>
    ENTRIES.find(e => (e.tweetId === tid) || (e.url || '').includes(tid))
  ).filter(Boolean);

  if (!winners.length) return;

  const medals = ['🥇', '🥈', '🥉'];
  const posClass = ['p1', 'p2', 'p3'];
  // Reorder for visual podium: 2nd - 1st - 3rd
  const order = [1, 0, 2];

  const cards = order.map(i => {
    const w = winners[i];
    if (!w) return '';
    const img = w.images && w.images[0] ? w.images[0] : '';
    const imgHtml = img
      ? `<img class="wm-img" src="${img}" alt="mascot" loading="eager">`
      : `<div class="wm-img" style="background:#1a1b2e;display:flex;align-items:center;justify-content:center;font-size:2rem">🎨</div>`;
    return `
      <a class="wm-card ${posClass[i]}" href="${w.url}" target="_blank" rel="noopener">
        ${imgHtml}
        <div class="wm-foot">
          <div class="wm-medal">${medals[i]}</div>
          <div class="wm-name">${w.author || w.handle}</div>
          <div class="wm-handle">${w.handle}</div>
          <div class="wm-likes">❤️ ${(w.likes || 0).toLocaleString()}</div>
        </div>
      </a>`;
  }).join('');

  document.getElementById('wmPodium').innerHTML = cards;
  document.getElementById('wm').classList.add('open');
  document.body.style.overflow = 'hidden';
}

function closeWinnerModal() {
  document.getElementById('wm').classList.remove('open');
  document.body.style.overflow = '';
}

/* ── Helpers ───────────────────────────────────────────────── */
function getFiltered() {
  if (!searchQuery) return ENTRIES;
  const q = searchQuery.toLowerCase();
  return ENTRIES.filter(e =>
    (e.author  || '').toLowerCase().includes(q) ||
    (e.handle  || '').toLowerCase().includes(q) ||
    (e.discordName || '').toLowerCase().includes(q)
  );
}

function getSorted() {
  const base = getFiltered();

  if (currentSort === 'likes') {
    // Pin winners at top (in order 1st/2nd/3rd), then rest by likes
    const winnerEntries = WINNER_TWEET_IDS.map(tid =>
      base.find(e => (e.tweetId === tid) || (e.url || '').includes(tid))
    ).filter(Boolean);
    const winnerIds = new Set(winnerEntries.map(e => e.id));
    const rest = [...base]
      .filter(e => !winnerIds.has(e.id))
      .sort((a, b) => b.likes - a.likes);
    return [...winnerEntries, ...rest];
  }

  // Sort by tweetId desc — Twitter snowflakes are time-ordered, higher = newer
  return [...base].sort((a, b) => {
    const idA = BigInt(a.tweetId || '0');
    const idB = BigInt(b.tweetId || '0');
    return idB > idA ? 1 : idB < idA ? -1 : 0;
  });
}

function rankClass(r) { return r === 1 ? 'rk1' : r === 2 ? 'rk2' : r === 3 ? 'rk3' : 'rkn'; }
function rankLabel(r) { return r === 1 ? '🥇 #1' : r === 2 ? '🥈 #2' : r === 3 ? '🥉 #3' : '#' + r; }
function likesClass(r) { return 'likes' + (r === 1 ? ' lk1' : r === 2 ? ' lk2' : r === 3 ? ' lk3' : ''); }

/** Avatar: prefer twitterAvatar, fall back to emoji initials */
function avatarHtml(entry) {
  if (entry.twitterAvatar) {
    return `<img src="${entry.twitterAvatar}" alt="" style="width:100%;height:100%;border-radius:50%;object-fit:cover;">`;
  }
  const ch = (entry.author || entry.handle || '?')[0].toUpperCase();
  return ch;
}

/* ── Image grid (card thumbnail) ────────────────────────────── */
function buildImgArea(entry) {
  const n = entry.images.length;

  const videoIcon = entry.hasVideo ? `<div class="video-icon">▶</div>` : '';

  if (n === 1) {
    return `
      <div class="img-area">
        <img class="img-single" src="${entry.images[0]}" alt="mascot" loading="lazy">
        ${videoIcon}
        <div class="view-ov">🔍</div>
      </div>`;
  }

  const gridClass = ['', '', 'c2', 'c3', 'c4'][Math.min(n, 4)];
  const thumbs = entry.images.slice(0, 4)
    .map(src => `<div class="img-thumb"><img src="${src}" alt="mascot" loading="lazy"></div>`)
    .join('');

  return `
    <div class="img-area">
      <div class="img-multi ${gridClass}">${thumbs}</div>
      ${videoIcon}
      <div class="img-count">📷 ${n}</div>
      <div class="view-ov">🔍</div>
    </div>`;
}

/* ── Render grid ────────────────────────────────────────────── */
function render() {
  const sorted = getSorted();
  document.getElementById('entryCount').textContent = ENTRIES.length;

  if (sorted.length === 0) {
    document.getElementById('grid').innerHTML =
      `<div class="no-results">No results for "${searchQuery}"</div>`;
    return;
  }

  const showRanks = currentSort === 'likes' && !searchQuery;

  document.getElementById('grid').innerHTML = sorted.map((entry, i) => {
    const rank = i + 1;
    const badge = showRanks
      ? `<div class="rank-badge ${rankClass(rank)}">${rankLabel(rank)}</div>`
      : '';
    return `
      <div class="card" onclick="openLightbox(${entry.id})">
        ${badge}
        ${buildImgArea(entry)}
        <div class="card-foot">
          <div class="author">
            <div class="ava">${avatarHtml(entry)}</div>
            <div>
              <div class="aname">${entry.author || entry.handle}</div>
              <div class="ahandle">${entry.handle}</div>
            </div>
          </div>
          <div class="${likesClass(rank)}">
            <span class="heart">❤️</span>${entry.likes.toLocaleString()}
          </div>
        </div>
      </div>`;
  }).join('');
}

/* ── Sort ───────────────────────────────────────────────────── */
function sortBy(type, el) {
  currentSort = type;
  document.querySelectorAll('.pill').forEach(p => p.classList.remove('active'));
  el.classList.add('active');
  render();
}

/* ── Search ─────────────────────────────────────────────────── */
function onSearch(val) {
  searchQuery = val.trim();
  document.getElementById('searchClear').classList.toggle('visible', searchQuery.length > 0);
  render();
}

function clearSearch() {
  searchQuery = '';
  document.getElementById('searchInput').value = '';
  document.getElementById('searchClear').classList.remove('visible');
  render();
}

/* ── Lightbox ───────────────────────────────────────────────── */
function openLightbox(id) {
  lbEntry = ENTRIES.find(e => e.id === id);
  lbIndex = 0;
  updateLightbox();
  document.getElementById('lb').classList.add('open');
  document.body.style.overflow = 'hidden';
}

function closeLightbox() {
  document.getElementById('lb').classList.remove('open');
  document.body.style.overflow = '';
}

function lbNext() {
  if (!lbEntry) return;
  lbIndex = (lbIndex + 1) % lbEntry.images.length;
  updateLightbox(true);
}

function lbPrev() {
  if (!lbEntry) return;
  lbIndex = (lbIndex - 1 + lbEntry.images.length) % lbEntry.images.length;
  updateLightbox(true);
}

function goToSlide(i) {
  lbIndex = i;
  updateLightbox(true);
}

function updateLightbox(animate = false) {
  if (!lbEntry) return;

  const img   = document.getElementById('lbImg');
  const multi = lbEntry.images.length > 1;

  if (animate) {
    img.classList.add('fade');
    setTimeout(() => {
      img.src = lbEntry.images[lbIndex];
      img.classList.remove('fade');
    }, 150);
  } else {
    img.src = lbEntry.images[lbIndex];
  }

  // Avatar
  const avaEl = document.getElementById('lbAva');
  if (lbEntry.twitterAvatar) {
    avaEl.innerHTML = `<img src="${lbEntry.twitterAvatar}" alt="" style="width:100%;height:100%;border-radius:50%;object-fit:cover;">`;
  } else {
    avaEl.textContent = (lbEntry.author || lbEntry.handle || '?')[0].toUpperCase();
  }

  document.getElementById('lbName').textContent   = lbEntry.author || lbEntry.handle;
  document.getElementById('lbHandle').textContent = lbEntry.handle;
  document.getElementById('lbLikes').textContent  = lbEntry.likes.toLocaleString();
  document.getElementById('lbLink').href          = lbEntry.url;

  // Nav arrows
  document.getElementById('lbNav').style.display = multi ? 'flex' : 'none';

  // Dots
  document.getElementById('lbDots').innerHTML = multi
    ? lbEntry.images.map((_, i) =>
        `<div class="lb-dot ${i === lbIndex ? 'on' : ''}" onclick="goToSlide(${i})"></div>`
      ).join('')
    : '';

  // Counter
  const counter = document.getElementById('lbCounter');
  if (multi) {
    counter.textContent  = `${lbIndex + 1} / ${lbEntry.images.length}`;
    counter.style.display = 'block';
  } else {
    counter.style.display = 'none';
  }
}

/* ── Keyboard & backdrop ────────────────────────────────────── */
document.getElementById('lb').addEventListener('click', function (e) {
  if (e.target === this) closeLightbox();
});

document.getElementById('wm').addEventListener('click', function (e) {
  if (e.target === this) closeWinnerModal();
});

document.addEventListener('keydown', e => {
  if (e.key === 'Escape') {
    if (document.getElementById('wm').classList.contains('open')) closeWinnerModal();
    else closeLightbox();
  }
  if (e.key === 'ArrowRight') lbNext();
  if (e.key === 'ArrowLeft')  lbPrev();
});

